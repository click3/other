	int maxEnergy2(std::vector<int> &weight) {
		int result = 0;
		for (int i = 1; i < weight.size()-1; i++) {
			const int temp = weight[i];
			const std::vector<int>::iterator it = weight.erase(weight.begin() + i);
			result = std::max(weight[i-1] * weight[i] + maxEnergy(weight), result);
			weight.insert(it, temp);
		}
		return result;
	}
	
